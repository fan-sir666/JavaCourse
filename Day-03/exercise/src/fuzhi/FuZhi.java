package fuzhi;

public class FuZhi {
    public static void main(String[] args) {
//        不同类型的赋值

//        整数
        int num = 1000;
        System.out.println(num);

//        浮点型
        double bb = 10.55;
        System.out.println(bb);

//        字符型
        char cc = '范';
        System.out.println(cc);

//        布尔型
        boolean dd = false;
        System.out.println(dd);

//        字符串型
        String ee = "范志伟";
        System.out.println(ee);

        /*
        *  注意：
        *   1. 变量可以重新赋值  次数不限
        *
        *   2. 赋值时 不能超出变量类型的规定范围  要赋值相同类型的
        *
        *   3. {} 是有变量的作用范围 变量可以由内向外 不可以由外向里去找
        * */
    }
}
